    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
            double payable = 0.0;
            int r = jTable1.getSelectedRow(),_advance = 0, _grossSalary = 0, _payableAmount = 0, _guestBill = 0,_absentDays = 0;
            String _pAmount = "", _gBill = "", _adv = "", 
                    _aDays = "";
            
            if(!String.valueOf(jTable1.getValueAt(r, 1)).equals("null"))
            {
                _aDays = String.valueOf(jTable1.getValueAt(r, 1));
            }
            if(!String.valueOf(jTable1.getValueAt(r, 4)).equals("null"))
            {
                _gBill = String.valueOf(jTable1.getValueAt(r, 4));
            }
            if(!String.valueOf(jTable1.getValueAt(r, 3)).equals("null"))
            {
                _adv = String.valueOf(jTable1.getValueAt(r, 3));
            }
            
            if(!_aDays.equals(""))
            {
                _absentDays = Integer.parseInt(_aDays);
            }
            if(!_gBill.equals(""))
            {
                _guestBill = Integer.parseInt(_gBill);
            }
            if(!_adv.equals(""))
            {
                _advance = Integer.parseInt(_adv);
            }
            
            _grossSalary = Integer.parseInt(String.valueOf(jTable1.getValueAt(r, 2)));
            payable = _grossSalary-((_grossSalary/30)*_absentDays);
            payable = payable - _guestBill;
            payable = payable - _advance;
            jTable1.setValueAt(payable, r, 5);
        }
    }//GEN-LAST:event_jTable1KeyPressed

