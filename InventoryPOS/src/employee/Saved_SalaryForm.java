    private void AddSalaryData(int id,String n, String d, String s, String adv,String gue,String pay,String re,int month)
    {
        String emp_name = n, absent_day=d, emp_salary=s, adv_salary=adv, guest_bill=gue, payable_amount=pay, remarks=re;
       
        //inserting data into transaction table
        PreparedStatement statement = null;
        try {
                statement = connection.prepareStatement
        ("INSERT INTO inv_salary (id, emp_name, absent_day, emp_salary, adv_salary, guest_bill, payable_amount, remarks,salary_month)"
                + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)");
                statement.setString(1, String.valueOf(id));
                statement.setString(2, emp_name);
                statement.setString(3, absent_day);
                statement.setString(4, emp_salary);
                statement.setString(5, adv_salary);
                statement.setString(6, guest_bill);
                statement.setString(7, payable_amount);
                statement.setString(8, remarks);
                statement.setString(9, String.valueOf(month));

                statement.execute();
            } catch (Exception e) {
                // TODO: handle exception
                e.printStackTrace();
                //System.out.println("Error while inserting data!");
                JOptionPane.showMessageDialog(rootPane, e.toString(),"Error",1);
            }finally
            {
                DatabaseUtils.close(statement);
            }
    }
